/* Сгенерировать 20 чисел от 1 до 20, каждый раз выводить
строку с соответствующим количеством * */

#include <iostream>
#include <ctime>

using namespace std;

int main()
{
    int k = 20;
    int a, ka;
    srand(time(0));
    for (int i=1; i <= k; i++)
    {
        a = rand()%20;
        cout << a << " ";
        for (int j = 0; j < a; j++)
            cout << "*";
        cout << endl;
    }
}

